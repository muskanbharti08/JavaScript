// There are three ways to create function

//  1. Function
//  2. function Expression
//  3. Arrow Function




//   FUNCTION

function fn1(){
        console.log("Function 1 ");
}
fn1();          // Function calling





// FUNCTION EXPRESSION

let fn2 = function(){
        console.log("Function Expression");
}
  fn2();





// ARROW FUNCTION

let fn3 = ()=>{
        console.log("Arrow Function");
}
fn3();